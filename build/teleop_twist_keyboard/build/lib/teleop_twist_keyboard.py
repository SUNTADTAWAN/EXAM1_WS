import sys
import threading

import geometry_msgs.msg
from std_msgs.msg import Bool

import rclpy
from pynput import keyboard

if sys.platform == 'win32':
    import msvcrt
else:
    import termios
    import tty


msg = """
---------------------------
Moving around:
   u    i    o
   j    k    l
   m    ,    .

For Holonomic mode (strafing), hold down the shift key:
---------------------------
   U    I    O
   J    K    L
   M    <    >

t : up (+z)
b : down (-z)

anything else : stop

q/z : increase/decrease max speeds by 10%
w/x : increase/decrease only linear speed by 10%
e/c : increase/decrease only angular speed by 10%

For Pizza command:
---------------------------
space bar : spawn pizza
    s     : save pizza
    r     : clear pizza


CTRL-C to quit
"""

moveBindings = {
    'i': (1, 0, 0, 0),
    'o': (1, 0, 0, -1),
    'j': (0, 0, 0, 1),
    'l': (0, 0, 0, -1),
    'u': (1, 0, 0, 1),
    ',': (-1, 0, 0, 0),
    '.': (-1, 0, 0, 1),
    'm': (-1, 0, 0, -1),
    'O': (1, -1, 0, 0),
    'I': (1, 0, 0, 0),
    'J': (0, 1, 0, 0),
    'L': (0, -1, 0, 0),
    'U': (1, 1, 0, 0),
    '<': (-1, 0, 0, 0),
    '>': (-1, -1, 0, 0),
    'M': (-1, 1, 0, 0),
    't': (0, 0, 1, 0),
    'b': (0, 0, -1, 0),
}

speedBindings = {
    'q': (1.1, 1.1),
    'z': (.9, .9),
    'w': (1.1, 1),
    'x': (.9, 1),
    'e': (1, 1.1),
    'c': (1, .9),
}

KeyBindings = {
    ' ': (True, False , False),    #spawn pizza
    's': (False, True , False),    #save  pizza
    'r': (False, False , True),    #reset pizza
    
} 


def getKey(settings):
    if sys.platform == 'win32':
        # getwch() returns a string on Windows
        key = msvcrt.getwch()
    else:
        tty.setraw(sys.stdin.fileno())
        # sys.stdin.read() returns a string on Linux
        key = sys.stdin.read(1)
        termios.tcsetattr(sys.stdin, termios.TCSADRAIN, settings)
    return key


def saveTerminalSettings():
    if sys.platform == 'win32':
        return None
    return termios.tcgetattr(sys.stdin)


def restoreTerminalSettings(old_settings):
    if sys.platform == 'win32':
        return
    termios.tcsetattr(sys.stdin, termios.TCSADRAIN, old_settings)


def vels(speed, turn):
    return 'currently:\tspeed %s\tturn %s ' % (speed, turn)


def main():
    settings = saveTerminalSettings()

    rclpy.init()

    node = rclpy.create_node('teleop_twist_keyboard')

    # parameters
    stamped = node.declare_parameter('stamped', False).value
    frame_id = node.declare_parameter('frame_id', '').value
    if not stamped and frame_id:
        raise Exception("'frame_id' can only be set when 'stamped' is True")

    if stamped:
        TwistMsg = geometry_msgs.msg.TwistStamped
    else:
        TwistMsg = geometry_msgs.msg.Twist

    pub = node.create_publisher(TwistMsg, 'cmd_vel', 10)
    pub_spawn = node.create_publisher(Bool, 'spawn_pizza_sub', 10)
    pub_save = node.create_publisher(Bool, 'save_pizza', 10)
    pub_clear = node.create_publisher(Bool, 'clear_pizza', 10)

    spinner = threading.Thread(target=rclpy.spin, args=(node,))
    spinner.start()

    speed = 0.5
    turn = 1.0
    x = 0.0
    y = 0.0
    z = 0.0
    th = 0.0
    status = 0.0

    spawn_pizza = False
    save_pizza  = False
    clear_pizza = False

    twist_msg = TwistMsg()
    bool_spawn_msg = Bool()
    bool_save_msg = Bool()
    bool_clear_msg = Bool()

    if stamped:
        twist = twist_msg.twist
        twist_msg.header.stamp = node.get_clock().now().to_msg()
        twist_msg.header.frame_id = frame_id
    else:
        twist = twist_msg

    try:
        print(msg)
        print(vels(speed, turn))
        while True:
            key = getKey(settings)
            if key in moveBindings.keys():
                x = moveBindings[key][0]
                y = moveBindings[key][1]
                z = moveBindings[key][2]
                th = moveBindings[key][3]
                spawn_pizza = False
                save_pizza = False
                clear_pizza = False
            elif key in speedBindings.keys():
                speed = speed * speedBindings[key][0]
                turn = turn * speedBindings[key][1]
                spawn_pizza = False
                save_pizza = False
                clear_pizza = False
                print(vels(speed, turn))
                if (status == 14):
                    print(msg)
                status = (status + 1) % 15
            elif key in KeyBindings.keys():
                spawn_pizza = KeyBindings[key][0]
                save_pizza = KeyBindings[key][1]
                clear_pizza = KeyBindings[key][2]     
            else:
                x = 0.0
                y = 0.0
                z = 0.0
                th = 0.0
                spawn_pizza = False
                save_pizza = False
                clear_pizza = False

                if (key == '\x03'):
                    break

            if stamped:
                twist_msg.header.stamp = node.get_clock().now().to_msg()

            twist.linear.x = x * speed
            twist.linear.y = y * speed
            twist.linear.z = z * speed
            twist.angular.x = 0.0
            twist.angular.y = 0.0
            twist.angular.z = th * turn
            bool_spawn_msg.data = spawn_pizza
            bool_save_msg.data = save_pizza
            bool_clear_msg.data = clear_pizza
            pub.publish(twist_msg)
            pub_spawn.publish(bool_spawn_msg)
            pub_save.publish(bool_save_msg)
            pub_clear.publish(bool_clear_msg)
            bool_spawn_msg.data = False
            bool_save_msg.data = False
            bool_clear_msg.data = False

    except Exception as e:
        print(e)

    finally:
        if stamped:
            twist_msg.header.stamp = node.get_clock().now().to_msg()

        twist.linear.x = 0.0
        twist.linear.y = 0.0
        twist.linear.z = 0.0
        twist.angular.x = 0.0
        twist.angular.y = 0.0
        twist.angular.z = 0.0
        bool_spawn_msg.data = False
        bool_save_msg.data = False
        bool_clear_msg.data = False
        pub.publish(twist_msg)
        pub_spawn.publish(bool_spawn_msg)
        pub_save.publish(bool_save_msg)
        pub_clear.publish(bool_clear_msg)

        rclpy.shutdown()
        spinner.join()

        restoreTerminalSettings(settings)


if __name__ == '__main__':
    main()
